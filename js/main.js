// Open Gallery
function openGallery(galleryId) {
    currentGallery = galleryId;
    currentSlide = 0;
    isPlaying = true;
    
    const data = galleryData[galleryId];
    document.getElementById('modalTitle').textContent = data.title;
    document.getElementById('galleryModal').classList.add('active');
    
    createDots(data.images.length);
    showSlide(0);
    startSlideshow();
    
    // Create sparkle effect
    createGallerySparkles();
}

// Close Gallery
function closeGallery() {
    document.getElementById('galleryModal').classList.remove('active');
    stopSlideshow();
    currentGallery = null;
}

// Create Dots
function createDots(count) {
    const dotsContainer = document.getElementById('slideshowDots');
    dotsContainer.innerHTML = '';
    
    for (let i = 0; i < count; i++) {
        const dot = document.createElement('span');
        dot.className = 'dot' + (i === 0 ? ' active' : '');
        dot.onclick = () => {
            stopSlideshow();
            showSlide(i);
            if (isPlaying) startSlideshow();
        };
        dotsContainer.appendChild(dot);
    }
}

// Show Slide
function showSlide(index) {
    if (!currentGallery) return;
    
    const data = galleryData[currentGallery];
    const images = data.images;
    
    // Wrap around
    if (index >= images.length) index = 0;
    if (index < 0) index = images.length - 1;
    
    currentSlide = index;
    
    const image = images[index];
    const slideImage = document.getElementById('slideImage');
    
    // ‡∏ñ‡πâ‡∏≤‡∏°‡∏µ src (‡∏£‡∏π‡∏õ‡∏à‡∏£‡∏¥‡∏á) ‡πÉ‡∏ä‡πâ img, ‡∏ñ‡πâ‡∏≤‡πÑ‡∏°‡πà‡∏°‡∏µ‡πÉ‡∏ä‡πâ emoji
    if (image.src) {
        slideImage.innerHTML = `<img src="${image.src}" alt="${image.caption}">`;
    } else {
        slideImage.innerHTML = image.emoji || 'üì∑';
    }
    
    document.getElementById('modalCaption').textContent = image.caption;
    
    // Update dots
    document.querySelectorAll('.dot').forEach((dot, i) => {
        dot.classList.toggle('active', i === index);
    });
    
    // Reset progress bar animation
    const progressBar = document.getElementById('progressBar');
    if (progressBar && isPlaying) {
        progressBar.classList.remove('animating');
        void progressBar.offsetWidth; // Force reflow
        progressBar.classList.add('animating');
    }
}

// Next/Prev Slide
function nextSlide() {
    stopSlideshow();
    showSlide(currentSlide + 1);
    if (isPlaying) startSlideshow();
}

function prevSlide() {
    stopSlideshow();
    showSlide(currentSlide - 1);
    if (isPlaying) startSlideshow();
}

// Play/Pause
function togglePlay() {
    isPlaying = !isPlaying;
    document.getElementById('playPauseBtn').textContent = isPlaying ? '‚è∏Ô∏è' : '‚ñ∂Ô∏è';
    
    if (isPlaying) {
        startSlideshow();
    } else {
        stopSlideshow();
    }
}

// Start Slideshow
function startSlideshow() {
    stopSlideshow();
    
    // Start progress bar animation
    const progressBar = document.getElementById('progressBar');
    progressBar.classList.remove('animating');
    void progressBar.offsetWidth; // Force reflow to restart animation
    progressBar.classList.add('animating');
    
    slideInterval = setInterval(() => {
        showSlide(currentSlide + 1);
        // Restart progress bar animation for new slide
        const bar = document.getElementById('progressBar');
        bar.classList.remove('animating');
        void bar.offsetWidth; // Force reflow
        bar.classList.add('animating');
    }, SLIDE_DURATION);
}

// Stop Slideshow
function stopSlideshow() {
    if (slideInterval) {
        clearInterval(slideInterval);
        slideInterval = null;
    }
    // Stop progress bar animation
    const progressBar = document.getElementById('progressBar');
    if (progressBar) {
        progressBar.classList.remove('animating');
    }
}

